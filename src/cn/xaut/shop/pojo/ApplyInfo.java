package cn.xaut.shop.pojo;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
@Entity
@Table(name ="TB_ApplyInfo")
@SequenceGenerator(name="Seqapply",sequenceName="tb_ApplyInfo_seq")
public class ApplyInfo implements Serializable {


	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Id
	@GeneratedValue(generator="Seqapply")
	private Integer idApplyInfo;//报名信息表id
	public Integer getIdApplyInfo() {
		return idApplyInfo;
	}
	public void setIdApplyInfo(Integer idApplyInfo) {
		this.idApplyInfo = idApplyInfo;
	}
	private String idUser;//报名信息表id
	private String userEmail;//申请人id
	private String timeApply;//申请时间
	private String reason;//报名意向
	private String fee;//状态1现场收费2网络收费
	private String state;//	状态
	private String checkEmail;//审核人
	private String timeCheck;//审核时间
	private String ideaCheck;//审核意见
	private String activityType;//活动类型
	private String memberType;//成员类型
	private int idApplication;//

  
	private Double budgetCostPre;//会员缴纳费用
	public String getIdUser() {
		return idUser;
	}
	public void setIdUser(String idUser) {
		this.idUser = idUser;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getTimeApply() {
		return timeApply;
	}
	public void setTimeApply(String timeApply) {
		this.timeApply = timeApply;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCheckEmail() {
		return checkEmail;
	}
	public void setCheckEmail(String checkEmail) {
		this.checkEmail = checkEmail;
	}
	public String getTimeCheck() {
		return timeCheck;
	}
	public void setTimeCheck(String timeCheck) {
		this.timeCheck = timeCheck;
	}
	public String getIdeaCheck() {
		return ideaCheck;
	}
	public void setIdeaCheck(String ideaCheck) {
		this.ideaCheck = ideaCheck;
	}
	public String getActivityType() {
		return activityType;
	}
	public void setActivityType(String activityType) {
		this.activityType = activityType;
	}
	public String getMemberType() {
		return memberType;
	}
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}
	public int getIdApplication() {
		return idApplication;
	}
	public void setIdApplication(int idApplication) {
		this.idApplication = idApplication;
	}
	public Double getBudgetCostPre() {
		return budgetCostPre;
	}
	public void setBudgetCostPre(Double budgetCostPre) {
		this.budgetCostPre = budgetCostPre;
	}
	
	
  
    
	
	
}
